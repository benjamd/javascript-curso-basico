let a = 3, b = 2, c = "3"

// '==': se revisa el valor sin importar el tipo
let z = a == b 
console.log(z)

z =  a == c
console.log(z)

//'===': revisa si los valores son iguales pero tambien los tipos
z =  a === c  
console.log(z)

/* ! : operador distinto */

z = a != b 
console.log(z)

z =  a != c
console.log(z)

//'!==': diferencia estrictao checkea tambien el tipo
z =  a !== c  
console.log(z)



