let a = 3, b = 2

//suma
let z = a + b
console.log("suma: "+ z)

//resta
z= a - b
console.log("resta: "+ z)

//multiplicacion
z = a * b
console.log("multiplicacion: "+ z)

//division
z = a / b
console.log("division: "+ z)

//modulo
z = a % b
console.log("modulo: "+ z)

//potencia
z = a ** b
console.log("potencia: "+ z)

//Imcremento
//Pre-Incremento (el operador ++ antes de la variable)
z = ++a;
console.log("a: "+ a)
console.log("z: "+ z)

//Post-Incremento (el operador ++ despues de la variable)
z = b++;
console.log("b: "+ b)
console.log("z: "+ z)

//Decremento
//Pre-Decremento
z = --a;
console.log("a: "+ a)
console.log("z: "+ z)

//Post-Decremento
z = b--;
console.log("b: "+ b)
console.log("z: "+ z)







